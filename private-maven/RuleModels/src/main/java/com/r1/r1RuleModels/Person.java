package com.r1.r1RuleModels;

public class Person {

    private int age;

    private boolean valid;

    public int getAge() {
        return this.age;
    }

    public void setAge(int a) {
        this.age = a;
    }

    public boolean getValid() {
        return this.valid;
    }

    public void setValid(boolean v) {
        this.valid = v;
    }
    
}
